    private void btnIniciarSecionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSecionActionPerformed
        // TODO add your handling code here:
        String usuario = txtUsuario.getText();
        char [] passwordAux = txtPassword.getPassword();
        
        String password = "";
        for( int i = 0; i < passwordAux.length; i++ ){
            
            password += passwordAux[ i ];
        }
        
        String seleccion = ( String ) jBoxTipoUsuario.getSelectedItem();
        boolean encontrado = false;
        
        for( int i = 0; i < Usuarios.usuarios.size(); i++ ){
            
            Usuario u = Usuarios.usuarios.get( i );
            
            if( usuario.equals( u.getNombreUsuario() ) && password.equals( u.getPassword() ) && seleccion.equals( "Administrador" ) &&  ( u instanceof Administrador ) ){
                
                Usuarios.usuarioActual = u;
                frmAdministrador a = new frmAdministrador();
                a.setVisible( true );
                encontrado = true;
                this.dispose();
                break;
                
            }else if( usuario.equals( u.getNombreUsuario() ) && password.equals( u.getPassword() ) && seleccion.equals( "Cliente") &&  ( u instanceof Cliente ) ){
                
                Usuarios.usuarioActual = u;
                frmCliente c = new frmCliente();
                c.setVisible( true );
                encontrado = true;
                this.dispose();
                break;
                
            }
        }
        
        if( !encontrado ){
            
            JOptionPane.showMessageDialog( null, "Usuario no reconocido", "Advertencia", JOptionPane.ERROR_MESSAGE );
        }
    }//GEN-LAST:event_btnIniciarSecionActionPerformed

